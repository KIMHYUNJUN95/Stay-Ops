type DeepPartial<T> = {
  [K in keyof T]?: T[K] extends (...args: infer Args) => infer Return
    ? (...args: Args) => Return
    : T[K] extends object
      ? DeepPartial<T[K]>
      : T[K];
};

function mergeDictionary<T extends Record<string, unknown>>(base: T, override: DeepPartial<T>): T {
  const result: Record<string, unknown> = { ...base };

  for (const [key, value] of Object.entries(override) as [keyof T, DeepPartial<T[keyof T]>][]) {
    const baseValue = base[key];
    if (
      value &&
      typeof value === "object" &&
      !Array.isArray(value) &&
      typeof baseValue === "object" &&
      baseValue &&
      !Array.isArray(baseValue)
    ) {
      result[key as string] = mergeDictionary(
        baseValue as Record<string, unknown>,
        value as Record<string, unknown>,
      );
    } else {
      result[key as string] = value;
    }
  }

  return result as T;
}

