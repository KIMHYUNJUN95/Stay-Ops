function withNavigation(locale: Locale) {
  const dictionary = mergeDictionary(FALLBACK_DICTIONARY, localeOverrides[locale]);

  return {
    ...dictionary,
    navigation: {
      admin: Object.fromEntries(
        Object.entries(localizedNavigationLabels.admin).map(([key, value]) => [key, value[locale]]),
      ),
      mobile: Object.fromEntries(
        Object.entries(localizedNavigationLabels.mobile).map(([key, value]) => [key, value[locale]]),
      ),
      utility: { notifications: localizedNavigationLabels.utility.notifications[locale] },
    },
  };
}

