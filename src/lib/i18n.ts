function withNavigation(locale: Locale) {
  const dictionary = mergeDictionary(FALLBACK_DICTIONARY, localeOverrides[locale]);

  return {
    ...dictionary,
    navigation: {
      admin: Object.fromEntries(
        Object.entries(localizedNavigationLabels.admin).map(([key, value]) => [key, value[locale]]),
      ),
      mobile: Object.fromEntries(
        Object.entries(localizedNavigationLabels.mobile).map(([key, value]) => [key, value[locale]]),
      ),
      utility: { notifications: localizedNavigationLabels.utility.notifications[locale] },
    },
  };
}

export type Dictionary = ReturnType<typeof withNavigation>;

export const dictionaries: Record<Locale, Dictionary> = {
  ko: withNavigation("ko"),
  ja: withNavigation("ja"),
  en: withNavigation("en"),
};

export function isLocale(value: string | null | undefined): value is Locale {
  return value === "ko" || value === "ja" || value === "en";
}

export function getDictionary(locale: Locale | string | null | undefined): Dictionary {
  if (isLocale(locale)) {
    return dictionaries[locale];
  }
  return dictionaries.ko;
}

export function getLocalizedText(
  text: LocalizedText | undefined | null,
  locale: Locale,
  fallback = "",
): string {
  if (!text) {
    return fallback;
  }
  return text[locale] ?? text.ko ?? text.en ?? fallback;
}
